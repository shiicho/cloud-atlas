# -----------------------------------------------------------------------------
# 严格模式（Lesson 09 会详细讲解）
# 取消注释以启用
# -----------------------------------------------------------------------------
# set -euo pipefail

# -----------------------------------------------------------------------------
# 全局变量
# -----------------------------------------------------------------------------

# 脚本所在目录（处理符号链接）
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# 脚本名称
SCRIPT_NAME="$(basename "$0")"

# 版本号
VERSION="1.0.0"

# -----------------------------------------------------------------------------
# 函数定义（Lesson 06 会详细讲解）
# -----------------------------------------------------------------------------

# 显示使用方法
usage() {
    echo "Usage: $SCRIPT_NAME [options]"
    echo ""
    echo "Options:"
    echo "  -h, --help     显示此帮助信息"
    echo "  -v, --version  显示版本号"
}

# 主函数
main() {
    echo "=========================================="
    echo "  脚本模板演示"
    echo "=========================================="
    echo ""
    echo "脚本路径: $SCRIPT_DIR"
    echo "脚本名称: $SCRIPT_NAME"
    echo "版本: $VERSION"
    echo ""
    echo "Hello from template!"
    echo ""
    echo "=========================================="
}

# -----------------------------------------------------------------------------
# 参数解析（Lesson 10 会详细讲解）
# -----------------------------------------------------------------------------

# 简单参数处理
case "${1:-}" in
    -h|--help)
        usage
        exit 0
        ;;
    -v|--version)
        echo "$SCRIPT_NAME version $VERSION"
        exit 0
        ;;
esac

# -----------------------------------------------------------------------------
# 执行主函数
# -----------------------------------------------------------------------------
main "$@"
