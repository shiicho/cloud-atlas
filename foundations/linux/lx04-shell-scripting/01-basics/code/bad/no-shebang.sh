# =============================================================================
# 反模式演示：缺少 shebang
# =============================================================================
#
# 问题：
#   这个脚本没有 shebang 行！
#   脚本的行为将依赖于当前 shell 是什么。
#
#   - 如果当前是 bash，会用 bash 执行
#   - 如果当前是 dash（Ubuntu 默认 /bin/sh），行为可能不同
#   - 如果当前是 zsh，也可能有差异
#
# 正确做法：
#   始终在第一行添加 #!/bin/bash 或 #!/usr/bin/env bash
#
# =============================================================================

# 这个数组语法只在 bash 中有效，在 dash/sh 中会报错
my_array=(one two three)
echo "数组元素: ${my_array[*]}"

# 如果用 dash 执行：
#   dash no-shebang.sh
# 会报错：Syntax error: "(" unexpected
