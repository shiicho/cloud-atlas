#!/bin/bash
# =============================================================================
# 脚本名称: system-info.sh
# 功能说明: 显示系统基本信息 - Mini Project 参考实现
# 作者: Cloud Atlas
# 创建日期: 2026-01-10
# =============================================================================
#
# 使用方法:
#   chmod +x system-info.sh
#   ./system-info.sh
#
# 功能:
#   - 显示主机名
#   - 显示当前用户
#   - 显示当前日期时间
#   - 显示内核版本
#   - 显示操作系统信息
#
# =============================================================================

echo "=========================================="
echo "         系统信息报告"
echo "=========================================="
echo ""

# 主机名
echo "主机名:     $(hostname)"

# 当前用户
echo "当前用户:   $(whoami)"

# 当前日期时间
echo "当前日期:   $(date '+%Y-%m-%d %H:%M:%S')"

# 内核版本
echo "内核版本:   $(uname -r)"

# 操作系统信息
# 使用 grep 和 cut 从 /etc/os-release 提取 PRETTY_NAME
if [[ -f /etc/os-release ]]; then
    os_name=$(grep PRETTY_NAME /etc/os-release | cut -d'"' -f2)
    echo "操作系统:   $os_name"
else
    echo "操作系统:   $(uname -s)"
fi

echo ""
echo "=========================================="
